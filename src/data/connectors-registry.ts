export function getConnectorById(id: string): DataConnector | undefined {
  return CONNECTOR_REGISTRY.find((c) => c.id === id);
}

export function getConnectorsByCategory(category: DataConnector["category"]): DataConnector[] {
  return CONNECTOR_REGISTRY.filter((c) => c.category === category);
}

export function getActiveConnectors(): DataConnector[] {
  return CONNECTOR_REGISTRY.filter((c) => c.status === "active" || c.status === "connector_ready");
}
